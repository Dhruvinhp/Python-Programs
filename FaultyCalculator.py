a = input("Enter first value: ")
b = input("Enter second value: ")
ans =input("Enter operation,e.g. +, -, *, /: ")

if ans == "+":
    print(int(a)+int(b))
elif ans == "-":
    print(int(a)-int(b))
elif ans == "*":
    print(int(a)*int(b))
elif ans=="/":
    print(int(a)/int(b))
else:
    print("plz enter valid operator")


