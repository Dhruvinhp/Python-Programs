str = 'this is a Simple String'

print(len(str)) #find lenth of string

print(str[:]) #print whole string
print(str[0:5]) #print string till 4 index
print(str[5:9]) #print defined string
print(str[0:23:2]) #third value is skip value
print(str[::-1]) #reverse string with help of -1 

print(str.find("sim")) #find the string
print(str.capitalize()) #capitalize first character
print(str.endswith("end")) #check whether string is ending with ""

print(str.count("i")) #count total char in given string

print(str.lower()) #convert whole string in lowercase
print(str.upper()) #convert whole string in uppercase

print(str.replace("Simple","hard")) #replace one string to another
